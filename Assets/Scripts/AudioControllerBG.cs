using UnityEngine;

public class AudioControllerBG : MonoBehaviour
{
    private static AudioControllerBG instance = null;

    void Awake()
    {
        if (instance == null)
        {
            instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else if (instance != this)
        {
            Destroy(gameObject);
        }
    }

}
